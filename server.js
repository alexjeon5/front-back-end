// 남이 미리 만들어둔 편리한 도구(라이브러리)들을 우리 주방으로 가져옵니다!
// express: 웹 서버(주방)를 아주 쉽게 만들게 해주는 마법 도구입니다.
// cors: 프론트엔드(손님)가 백엔드와 안전하게 대화할 수 있도록 출입증을 발급해주는 도구예요.
const express = require('express');
const cors = require('cors');

// 'app'이라는 이름의 메인 서버 프로그램(우리의 식당)을 차립니다.
const app = express();

// 우리 식당이 손님을 기다릴 문 번호(포트 번호)를 5000번으로 정해둡니다.
const PORT = 5000;

// cors 도구를 사용해서, 다른 주소(프론트엔드 화면)에서 오는 간식 요청을 막지 않고 통과시켜 줍니다.
app.use(cors());

// 📋 간식 룰렛 메뉴판입니다! 대괄호 [] 안에 여러 메뉴들을 모아두었어요.
const menus = ["🔥 마라탕", "🍓 탕후루", "🍜 편의점 컵라면", "🌶️ 학교 앞 떡볶이", 
  "🥟 동성로 납작만두", "🛑 오늘은 굶어라!"];

// 손님(프론트엔드)이 '/api/snack' 이라는 주소로 "간식 주세요!"라고 요청(GET)을 보내면 실행되는 부분입니다.
app.get('/api/snack', (req, res) => {
  
  // 🎲 주사위를 굴려서(Math.random) 메뉴판에 있는 간식 중 랜덤으로 하나를 쏙 뽑습니다.
  const randomSnack = menus[Math.floor(Math.random() * menus.length)];

  // 사장님(개발자)만 볼 수 있는 서버 컴퓨터의 까만 화면(터미널)에 몰래 기록을 남깁니다. 
  console.log(`[알림] 프론트엔드에서 간식을 요청했습니다! 결과: ${randomSnack}`);

  // 손님에게 뽑힌 간식을 예쁜 상자(JSON 형식)에 담아서 대답(응답, res)해줍니다.
  res.json({ 
    text: randomSnack 
  });
});

// 5000번 문(포트)을 열고 진짜로 서버를 켭니다! 
// '0.0.0.0'은 누구든 우리 식당에 들어올 수 있게 문을 활짝 여는 주문이에요.
app.listen(PORT, '0.0.0.0', () => {
  console.log(`🚀 간식 서버 가동 중! 포트 번호: ${PORT}`);
});
